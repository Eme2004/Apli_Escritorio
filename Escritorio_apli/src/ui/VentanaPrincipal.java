    // Método para convertir una cadena de texto en un objeto LocalDate
// Espera que la fecha esté en formato "MM/dd/yyyy" (mes/día/año)
private LocalDate parseFecha(String fechaStr) {
    // Si la cadena es nula o vacía, retorna null (sin fecha)
    if (fechaStr == null || fechaStr.trim().isEmpty()) return null;

    // Define el formato esperado para la fecha
    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("MM/dd/yyyy");

    try {
        // Intenta convertir la cadena en LocalDate usando el formato definido
        return LocalDate.parse(fechaStr.trim(), formatter);
    } catch (DateTimeParseException e) {
        // Si el formato es incorrecto, lanza una excepción con mensaje claro
        throw new IllegalArgumentException("Formato de fecha inválido. Use: MM/dd/yyyy");
    }
}

