    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
         try {
        String titulo = txtTitulo.getText();
        int prioridad = cmbPrioridad.getSelectedIndex() + 1;
        boolean especial = chkEspecial.isSelected();
        LocalDate fecha = parseFecha(txtFecha.getText());

        if (titulo == null || titulo.trim().isEmpty()) {
            throw new IllegalArgumentException("El título no puede estar vacío.");
        }

        GestorTareas gestor = GestorTareas.obtenerInstancia();
        gestor.crearTarea(titulo.trim(), prioridad, fecha, especial);

        JOptionPane.showMessageDialog(this,
                "Tarea guardada exitosamente.",
                "Éxito",
                JOptionPane.INFORMATION_MESSAGE);

        limpiarFormulario();

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "Validación",
                JOptionPane.ERROR_MESSAGE);
    }
         
    }//GEN-LAST:event_btnGuardarActionPerformed

